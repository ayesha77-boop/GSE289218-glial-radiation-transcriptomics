# CLEAN GENE LIST: Remove lncRNAs, pseudogenes, Ensembl IDs
def clean_gene_list(gene_list):
    cleaned = []
    for g in gene_list:

        if g.startswith('ENSG'): continue

        if 'LINC' in g: continue

        if g.endswith('-AS1') or g.endswith('-AS2'): continue
        if g.endswith('-IT1') or g.endswith('-IT2'): continue

        if '-PS' in g: continue

        if g.startswith('MT-'): continue

        cleaned.append(g)
    return cleaned


