"""# **Volcano plots - Control vs test**"""

def volcano_plot(ax, df, title, n_labels=15):
    df = df.copy()
    df['-log10padj'] = -np.log10(df['padj'].replace(0, 1e-300))
    for cat, color in palette.items():
        sub = df[df['sig'] == cat]
        ax.scatter(sub['log2FC'], sub['-log10padj'],
                   c=color, s=8, alpha=0.7, label=cat, rasterized=True)

    top = df[df['sig'] != 'Not significant'].nlargest(n_labels, '-log10padj')
    texts = [ax.text(row['log2FC'], row['-log10padj'], idx,
                     fontsize=6, fontweight='bold')
             for idx, row in top.iterrows()]
    adjust_text(texts, ax=ax, arrowprops=dict(arrowstyle='-', color='grey', lw=0.5))

    ax.set_xlabel('log₂ Fold Change', fontsize=11)
    ax.set_ylabel('-log₁₀ (adj. p-value)', fontsize=11)
    ax.set_title(title, fontsize=13, fontweight='bold')
    ax.legend(fontsize=9, markerscale=2)


