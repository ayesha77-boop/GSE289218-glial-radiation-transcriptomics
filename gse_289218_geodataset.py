def ma_plot(ax, df, title):
    # A = average expression approximated from pct.1 and pct.2
    df = df.copy()
    df['A'] = (df['pct.1'] + df['pct.2']) / 2

    for cat, color in palette.items():
        sub = df[df['sig'] == cat]
        ax.scatter(sub['A'], sub['log2FC'],
                   c=color, s=8, alpha=0.7, label=cat, rasterized=True)

    ax.axhline( FC_THRESH,  color='grey', lw=0.8, ls='--')
    ax.axhline(-FC_THRESH,  color='grey', lw=0.8, ls='--')
    ax.axhline(0, color='black', lw=0.6)
    ax.set_xlabel('Average Detection Rate (A)', fontsize=11)
    ax.set_ylabel('log₂ Fold Change (M)', fontsize=11)
    ax.set_title(title, fontsize=13, fontweight='bold')
    ax.legend(fontsize=9, markerscale=2)

