df_2wk  = pd.read_csv('/2wk_vs_Control.csv',  index_col=0)
df_72hr = pd.read_csv('/72hr_vs_Control.csv', index_col=0)

for df, label in [(df_2wk, '2wk'), (df_72hr, '72hr')]:
    df.rename(columns={
        'avg_log2FC': 'log2FC',
        'p_val':      'pval',
        'p_val_adj':  'padj'
    }, inplace=True)

df_2wk['group']  = '2wk'
df_72hr['group'] = '72hr'

FC_THRESH  = 1.0   # |log2FC| cutoff
ADJ_THRESH = 0.05  # adjusted p-value cutoff

def classify(df):
    """Add significance category column."""
    conds = [
        (df['padj'] < ADJ_THRESH) & (df['log2FC'] >  FC_THRESH),
        (df['padj'] < ADJ_THRESH) & (df['log2FC'] < -FC_THRESH),
    ]
    choices = ['Up Regulated', 'Down Regulated']
    df['sig'] = np.select(conds, choices, default='Not significant')
    return df

df_2wk  = classify(df_2wk)
df_72hr = classify(df_72hr)

palette = {'Up Regulated': '#E63946', 'Down Regulated': '#457B9D', 'Not significant': '#A8DADC'}

