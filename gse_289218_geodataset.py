def compare_enrichment(ct1_res, ct2_res, ct1_name, ct2_name, timepoint):
    if ct1_res is None or ct2_res is None:
        print(f"  Skipping comparison — missing results for {timepoint}")
        return


    top_ct1 = set(ct1_res.nsmallest(15, 'Adjusted P-value')['Term_short'].tolist())
    top_ct2 = set(ct2_res.nsmallest(15, 'Adjusted P-value')['Term_short'].tolist())
    shared  = top_ct1.union(top_ct2)

    rows = []
    for term in shared:
        r1 = ct1_res[ct1_res['Term_short'] == term]
        r2 = ct2_res[ct2_res['Term_short'] == term]
        rows.append({
            'Term':       term,
            f'{ct1_name}_log10p': -np.log10(r1['Adjusted P-value'].values[0]) if len(r1) else 0,
            f'{ct2_name}_log10p': -np.log10(r2['Adjusted P-value'].values[0]) if len(r2) else 0,
        })

    comp = pd.DataFrame(rows).set_index('Term')
    comp = comp.sort_values(f'{ct1_name}_log10p', ascending=False).head(20)

    fig, ax = plt.subplots(figsize=(8, max(6, len(comp)*0.4)))
    x = np.arange(len(comp))
    w = 0.35
    ax.barh(x - w/2, comp[f'{ct1_name}_log10p'], w,
            color='#E63946', alpha=0.85, label=ct1_name)
    ax.barh(x + w/2, comp[f'{ct2_name}_log10p'], w,
            color='#457B9D', alpha=0.85, label=ct2_name)
    ax.set_yticks(x)
    ax.set_yticklabels(comp.index, fontsize=8)
    ax.set_xlabel('-log₁₀ (Adjusted P-value)', fontsize=11)
    ax.set_title(f'Neuroinflammatory Pathway Comparison\n{ct1_name} vs {ct2_name} | {timepoint}',
                 fontsize=12, fontweight='bold')
    ax.axvline(x=-np.log10(0.05), color='black', lw=0.8, ls='--', alpha=0.5)
    ax.legend(fontsize=10)
    plt.tight_layout()
    fname = f'Pathway_Comparison_{timepoint}.png'
    plt.savefig(fname, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"Saved: {fname}")


